using Microsoft.EntityFrameworkCore;
using RFI.FRI.Api.Domain.Modal;
using RFI.FRI.Application.Common;
using RFI.FRI.Domain.Repositories;

namespace RFI.FRI.Infrastructure.Repositories
{
    public class UserRepository : IUserRepository
    {
        private readonly FRIdbContext _context;

        public UserRepository(FRIdbContext context)
        {
            _context = context;
        }

        public async Task<User> GetUserById(int id)
        {
            return await _context.Users.FindAsync(id);
        }

        public async Task<IEnumerable<User>> GetAllUsers()
        {
            return await _context.Users.ToListAsync();  
        }

        public async Task<User> AddUser(User user)
        {
            _context.Users.Add(user);
            await _context.SaveChangesAsync();
            return user;
        }

        public async Task<User> UpdateUser(User user)
        {
            _context.Users.Update(user);
            await _context.SaveChangesAsync();
            return user;
        }

        public async Task DeleteUser(int id)
        {
            var user = await _context.Users.FindAsync(id);
            if (user != null)
            {
                _context.Users.Remove(user);
                await _context.SaveChangesAsync();
            }
        }
    }
}
 
